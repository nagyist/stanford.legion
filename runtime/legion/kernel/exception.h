#define REPORT_LEGION_FATAL(code, fmt, ...)          \
  {                                                  \
    char message[4096];                              \
    snprintf(message, 4096, fmt, ##__VA_ARGS__);     \
    Legion::Internal::Runtime::report_fatal_message( \
        code, __FILE__, __LINE__, message);          \
  }

